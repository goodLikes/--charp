// Находим все числа стоящие до N

int N = 100;
int a = 0;

while (a <= N)
{
  // есть виды вывода в консоль, каждый раз будет выводить на новой строке : Console.WriteLine(a);
  // Будет выводить одно за другим пример: "12345678910111213" Console.Write(a);
  Console.Write(a);
  Console.Write(' '); // создает пробел между цифрами
  a++;
}



